public class BinaryTest{
    public static void main(String[] args) {
        //정수를 많이 표현하는 자료형 : int 
        int num = 10;
        int bNum = 0B1010;
        int oNum = 012;
        int xNum = 0xA;

        System.out.println(num); //10진수 
        System.out.println(bNum); //바이너리
        System.out.println(oNum); //8진수
        System.out.println(xNum); //16진수

    }
}