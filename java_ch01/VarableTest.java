public class VarableTest {
    public static void main(String[] args) {
        int age;
        age=10;

        int level=10000;

        System.out.println(age);
        System.out.println(level);

        /*
        변수 이름은 대문자나 수자를 사용할수 있고, 특수문자는 &,_만 사용할수 있다.
        변수의 이름은 숫자로 할 수 없음
        자바에서 이미 사용하는 예약어는 사용할수 없음 
        변수 이름은 프로그램내에서 사용되는것이므로 그 용도에 맞고 가독성(중요)이 좋게 만드는것이 중요함(미래의 내가 욕하지 않도록 만드는 것이 중요)
        변수의 길이는 관련이 없다. 대신 어떤것을 의미하는지 정확히 의미 해야 한다. 
        요새 추세는 소문자로 시작을 해서 중간중간 대문자를 섞어 주는 추세이다. 
        */
    }
}
